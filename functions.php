function set_cot_par( $data, $cota_par, $faixa ) {
  if ($faixa == "_0") {
    return 500.00;
  }
  if ($faixa == "") {
    return 800.00; 
  }
  return ( calc_cota( $data['valor'], $cota_par ) > 500 ) ? calc_cota( $data['valor'], $cota_par ) : 500.00;
}

function set_calc( $data ) {
  $post_id          = 570;
  $faixa            = faixa( floatval( str_replace( ",", ".", str_replace( ".", "", $data['valor'] ) ) ) );
  $type             = ( $data['tipo'] ) ? floatval( str_replace( ",", ".", get_field( $data['tipo'] . $faixa, $post_id ) ) ) : 0;
  $cota_par         = floatval( str_replace( ",", ".", get_field( "cota_par_" . $data['tipo'] . $faixa, $post_id ) ) );
  $furto_roubo      = ( $data['furto_roubo'] == "true" ) ? floatval( str_replace( ",", ".", get_field( "furto_roubo_" . $data['tipo'] . $faixa, $post_id ) ) ) : 0;
  $cota_furto_roubo = ( $data['furto_roubo'] == "true" ) ? floatval( str_replace( ",", ".", get_field( "cota_furto_roubo_" . $data['tipo'] . $faixa, $post_id ) ) ) : 0;
  $plano            = ( ( $data['furto_roubo'] == "false" ) ? $type : 0 ) + floatval( str_replace( ",", ".", get_field( "plano_" . $data['plano'] , $post_id ) ) ) + $furto_roubo + adicionais( $data );
  $adesao           = ( $data['furto_roubo'] == "false" ) ? floatval( str_replace( ",", ".", get_field( "adesao_" . $data['tipo'], $post_id ) ) ) : floatval( str_replace( ",", ".", get_field( "adesao_furto_roubo", $post_id ) ) );
  return array( 
    "nome" => $data['nome'],
    "telefone" => $data['telefone'],
    "veiculo" => $data['veiculo'],
    "valor_veiculo" => floatval( str_replace( ",", ".", str_replace( ".", "", $data['valor'] ) ) ),
    "codigo_fipe" => $data['codigo_fipe'], 
    "tipo" => $data['tipo'],
    "adesao" => ( $data["sistema_rastreamento"] == "true" || $data["sistema_rastreamento_bloqueio"] == "true") ? 150 + $adesao : 0 + $adesao,
    "tipo_valor" => $type, 
    "cota_par" => set_cot_par( $data, $cota_par, $faixa ),
    "furto_roubo" => $data['furto_roubo'], 
    "cota_furto_roubo" => ( $data['furto_roubo'] == "true" ) ? ( calc_cota( $data['valor'], $cota_furto_roubo ) > 800 ) ? calc_cota( $data['valor'], $cota_furto_roubo ) : 800.00 : 0,
    "valor_plano" => ($data['desconto'] != "") ? ( $plano - ( $plano * ( floatval( $data['desconto'] ) / 100 ) ) ) : $plano,
    "plano" => $data['plano'],
    "desconto" => $data['desconto'],
    "coberturas" => array(
      "socorro" => get_field( "socorro_" . $data['plano'], $post_id ),
      "retorno" => get_field( "retorno_" . $data['plano'], $post_id ),
      "reboque" => get_field( "reboque_" . $data['plano'], $post_id ),
      "pneus" => get_field( "pneus_" . $data['plano'], $post_id ),
      "combustivel" => get_field( "combustivel_" . $data['plano'], $post_id ),
      "guarda" => get_field( "guarda_" . $data['plano'], $post_id ),
      "chaveiro" => get_field( "chaveiro_" . $data['plano'], $post_id ),
      "reboque_pane" => get_field( "reboque_pane_" . $data['plano'], $post_id ),
      "taxi" => get_field( "taxi_" . $data['plano'], $post_id ),
      "hospedagem" => get_field( "hospedagem_" . $data['plano'], $post_id ),
      "carro_reserva" => get_field( "carro_reserva_" . $data['plano'], $post_id ),
      "ap" => get_field( "ap_" . $data['plano'], $post_id ),
      "app" => get_field( "app_" . $data['plano'], $post_id ),
      "desconto" => get_field( "desconto_" . $data['plano'], $post_id ),
    ),
    "adicionais" => set_add( $data ),
  );
}


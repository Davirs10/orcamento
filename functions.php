function pdfGeneration($info) {
  ob_start();
  $infos = $info;
  require_once('includes/templates/contentCotacaoPDF.php');
  $template = ob_get_clean();
  require_once('dompdf/autoload.inc.php');
  
  $webRoot = get_template_directory_uri();
  $dompdf = new Dompdf\Dompdf();
  $dompdf->setBasePath($webRoot);
  $dompdf->loadHtml($template);

  $customPaper = array(0,0,810,1440);
  $dompdf->setPaper( $customPaper );
  $dompdf->render();
  $dompdf->stream('cotação__'.date('d').date('m').date('Y'));
}

function faixa($valor) {
  if ($valor < 5000.01) return "_0";
  if ($valor > 5000.01  && $valor <= 10000.00) return "";
  if ($valor > 10000.01 && $valor <= 20000.00) return "_ii";
  if ($valor > 20000.01 && $valor <= 30000.00) return "_iii";
  if ($valor > 30000.01 && $valor <= 40000.00) return "_iv";
  if ($valor > 40000.01 && $valor <= 50000.00) return "_v";
  if ($valor > 50000.01 && $valor <= 60000.00) return "_vi";
  if ($valor > 60000.01 && $valor <= 70000.00) return "_vii";
}

function calc_cota($value, $percent) {
  return floatval( str_replace( ",", ".", str_replace( ".", "", $value ) ) ) * ( $percent / 100);
}

function adicionais( $add ) {
  $result = 0;
  $result += ( $add["acidentes_pessoais_premiado"] == "true" ) ? 2.00 : 0;
  $result += ( $add["clube_vantagens"] == "true" ) ? 2.00 : 0;
  $result += ( $add["acidentes_passageiros_10"] == "true" ) ? 10.00 : 0;
  $result += ( $add["sistema_rastreamento"] == "true" ) ? 30.00 : 0;
  $result += ( $add["sistema_rastreamento_bloqueio"] == "true" ) ? 49.90 : 0;
  $result += ( $add["reboque_100"] == "true" ) ? 10.00 : 0;
  $result += ( $add["carro_reserva_5"] == "true" ) ? 10.00 : 0;
  $result += ( $add["cobertura_terceiros_5"] == "true" ) ? 9.90 : 0;
  return $result;
}

